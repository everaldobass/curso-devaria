# if - Verifica se a condição inteira passada entre parenteses e verdadeira

nome =  "Edson"

if nome == "Everaldo":
    print(f"Nome: {nome}")
elif nome == "Edson":
    print(f"Seu nome é: {nome}")
else:
    print("Seu nome é outro: ")