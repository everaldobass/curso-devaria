# Estruturas de repetição

print("\n------------FOR--------------------")
for n in range(0,3):
    print(n)

print("\n------------Lista--------------------")
# Para iterar sobre uma lista:
lista = [1,2,3,4]
for numero in lista:
    print(numero)


print("\n------------String--------------------")
# Para iterar sobre uma Palavras:
palavra = "Everaldo"
for letra in palavra:
    print(letra)

